public class PDF {

    public String filename;

    public String tag;

    public String description;

    public int year;

    public int month;



    public PDF(){}



    public PDF(String filename, String tag, String description, int year, int month){

        this.filename = filename;

        this.tag = tag;

        this.description = description;

        this.year = year;

        this.month = month;

    }

}